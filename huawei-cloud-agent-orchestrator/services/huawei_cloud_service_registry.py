@dataclass
class ServiceInfo:
    """服务信息"""
    name: str
    description: str
    module_name: str
    client_class: str
    common_operations: List[str]
    usage: str = ""

    @property
    def sdk_package_name(self) -> str:
        """SDK包名"""
        return self.module_name

    @property
    def operations_count(self) -> int:
        """操作数量"""
        return len(self.common_operations)

    @property
    def operations(self) -> List[str]:
        """操作列表（兼容旧代码）"""
        return self.common_operations


